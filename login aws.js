//simple  login page that asks user id & password
function login() { 
    var id = prompt("Enter your ID");
    var password = prompt("Enter your password");
    if (id == "admin" && password == "1234") {
        alert("Welcome to the admin page");
    }
    else {
        alert("Wrong ID or password");
    }
}    

